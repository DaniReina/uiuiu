<p>

  <h3>DATOS PERSONALES</h3>
  Sus datos personales serán tratados con base a en los principios de licitud, consentimiento, 
  información, calidad, finalidad, lealtad, proporcionalidad y responsabilidad en términos de la 
  Legislación. Se mantendrá la confidencialidad de sus datos personales estableciendo y manteniendo de 
  forma efectiva las medidas de seguridad administrativas, técnicas y físicas, para evitar su daño, 
  pérdida, alteración, destrucción, uso, acceso o divulgación indebida.

</p><p>

  <h3>QUE SON LAS COOKIES Y COMO SE UTILIZAN</h3>
  Los cookies son pequeñas piezas de información que son enviadas por el sitio web a su navegador y se 
  almacenan en el disco duro de su equipo y se utilizan para determinar sus preferencias cuando se 
  conecta a los servicios de nuestros sitios, así como para rastrear determinados comportamientos o 
  actividades llevadas a cabo por usted dentro de nuestros sitios.
  <p>
  En algunas secciones de nuestro sitio requerimos que el cliente tenga habilitados los cookies ya que 
  algunas de las funcionalidades requieren de estas para trabajar. </p>
  <p>
  Los cookies nos permiten:
  </br>
  a) Reconocerlo al momento de entrar a nuestros sitios y ofrecerle de una experiencia personalizada
  </br> 
  b) Conocer la configuración personal del sitio especificada por usted, por ejemplo, 
  los cookies nos permiten detectar el ancho de banda que usted ha seleccionado al momento de ingresar 
  la página de inicio de nuestro sitio, de tal forma que sabemos qué tipo de información es aconsejable 
  descargar 
  </br> 
  c) Calcular el tamaño de nuestra audiencia y medir algunos parámetros de tráfico, pues cada navegador 
  que obtiene acceso a nuestros sitios adquiere un cookie que se usa para determinar la frecuencia de 
  uso y las secciones de los sitios visitadas, reflejando así sus hábitos y preferencias, información 
  que nos es útil para mejorar el contenido, los titulares y las promociones para los usuarios. Los 
  cookies le permitirán tomar ventaja de las características más benéficas que le ofrecemos, por lo que 
  le recomendamos que las deje activadas.
  </br>
  La utilización de cookies no será utilizada para identificar a los usuarios, con excepción de los casos en que se investiguen posibles actividades fraudulentas.
  </p>

</p><p>

  <h3>DERECHOS ARCO (ACCESO, RECTIFICACIÓN, CANCELACIÓN Y OPOSICIÓN)</h3>
  El área responsable del manejo y la administración de los datos personales es: Servicio al Cliente a 
  quien puede contactar mediante en el correo electrónico libreriaHDA-SC@gmail.com.

</p><p>

  <h3>PARA EJERCER SUS DERECHOS ARCO Y REVOCAR EL CONSENTIMIENTO OTORGADO</h3>
  <p>
  Como titular de datos personales, el “Usuario” podrá ejercitar los derechos ARCO (acceso, cancelación,
  rectificación y oposición al tratamiento de sus datos personales), o bien, revocar el consentimiento 
  que haya otorgado a nuestro sitio, para el tratamiento de sus datos personales, enviando directamente 
  su solicitud a través de la cuenta de correo electrónico: libreriaHDA-ARCO@gmail.com
  </p><p>
  Dicha solicitud deberá contener por lo menos: (a) nombre y correo u otro medio para comunicarle la 
  respuesta a su solicitud; (b) los documentos que acrediten su identidad o, en su caso, la 
  representación legal; (c) la descripción clara y precisa de los datos personales respecto de los que 
  se solicita ejercer alguno de los derechos ARCO, (d) la manifestación expresa para revocar su 
  consentimiento al tratamiento de sus datos personales y por tanto, para que no se usen; (d) cualquier 
  otro elemento que facilite la localización de los datos personales. Su petición deberá ir acompañada 
  de los fundamentos por los que solicita dicha revocación y una identificación oficial del titular de 
  los datos o de su apoderado. En un plazo máximo de 20 días hábiles atenderemos su solicitud y le 
  informaremos sobre la procedencia de la misma a través del correo electrónico del que provenga la 
  petición. Se solicita al usuario que actualice sus datos cada vez que éstos sufran alguna modificación,
  ya que esto permitirá brindarle un servicio eficiente y personalizado.
  </p>

</p><p>

  <h3>PROTECCIÓN</h3>
  La seguridad y la confidencialidad de los datos que los usuarios proporcionen al contratar un servicio 
  o comprar un producto en línea en nuestro sitio, estarán protegidos por un servidor seguro bajo el 
  protocolo Secure Socket Layer (SSL), de tal forma que los datos enviados se transmitirán encriptados 
  para asegurar su resguardo.
  </br>
  Nuestro proveedor de plataforma de Pagos en línea, PayPal, maneja el SSL, y es una empresa con mucha 
  experiencia y excelente reputación en manejo de información de pago de Clientes.

</p><p>

  <h3>BASES DE DATOS</h3>
  La Base de Datos de la Empresa, donde se almacena la información recabada, mantendrá guardada y 
  resguardada la Información proporcionada por el Titular, a pesar que sean modificadas, actualizadas, 
