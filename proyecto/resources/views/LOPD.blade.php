  <h3>TRANSFERENCIA DE INFORMACIÓN CON TERCEROS</h3>
  <p>
  El Titular autoriza expresamente a la Empresa a mantener en su registro las informaciones 
  proporcionadas por el Titular, también autoriza a la Empresa a proporcionar información constante 
  sobre el referido registro a (i) autoridades que lo solicitaren conforme lo permitido por la 
  legislación en vigor y (ii) a sus asociados estratégicos, comerciales o técnicos con la finalidad de 
  ofrecer mejores condiciones de Promociones y/o contenidos del Usuario. Además, el Usuario permite 
  expresamente a la Empresa a recopilar información para la realización de seguimiento de tráfico, con 
  intención de identificar grupos y perfiles de usuarios, así como para fines de orientación publicitaria.
  </p><p>
  Las Transferencias de Datos son con fines lícitos y acorde a lo señalado en la Ley de Protección de
  Datos Personales en Posesión de los Particulares.
  </p><p>
  La Empresa declara que no transferirá ningún tipo de información del Titular a Terceros nacionales o 
  extranjeros. En caso contrario, se le hará del conocimiento al Titular mediante la publicación de un 
  nuevo aviso de Privacidad. Lo anterior de acuerdo con lo previsto en el artículo 36 del Reglamento de 
  la Ley de Protección de Datos Personales en Posesión de Particulares.
  </p>

