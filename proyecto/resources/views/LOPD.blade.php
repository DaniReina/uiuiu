  <h3>CONFIDENCIALIDAD</h3>
  <p>
  Los datos de los Titulares serán suministrados únicamente por la Empresa en las formas establecidas en 
  este Aviso de Privacidad. La Empresa hará todo lo que esté a su alcance para proteger la privacidad 
  de la información. Puede suceder que en virtud de órdenes judiciales, o de regulaciones legales, la 
  Empresa se vea compelida a revelar información a las autoridades o terceras partes bajo ciertas 
  circunstancias, o bien en casos que terceras partes puedan interceptar o acceder a cierta información 
  o transmisiones de datos en cuyo caso la Empresa no responderá por la información que sea revelada. 
  En estos casos, la Empresa notificará al Titular sobre esta situación.
  </p><p> 
  No asumimos ninguna obligación de mantener confidencial cualquier otra información que el Titular 
  proporcione a través de boletines y pláticas en línea (chats), así como la información que obtenga a 
  través de los cookies, información que no haya sido directamente entregada a la Empresa.
  </p><p>
  Se debe aclarar que NO se vende, regala, facilita ni alquila la información del Titular a ningún 
  tercero. Si el usuario no desea que sus datos sean compartidos, puede decidir NO utilizar un servicio 
  determinado o NO participar en algunas promociones o concursos.
  </p><p>
  La Empresa tomará todas las medidas posibles para mantener la confidencialidad y la seguridad 
  descritas anteriormente, pero no responderá por los daños o perjuicios que pudieran derivarse de la 
  violación de esas medidas por parte de terceros que utilicen las redes públicas o el Internet, 
  alterando los sistemas de seguridad para obtener acceso a la información de los Titulares.
  </p><p>
  Las vulneraciones de seguridad ocurridas en cualquier fase del tratamiento que afecten de forma 
  significativa los derechos patrimoniales o morales de los Titulares, serán informadas de forma 
  inmediata por la Empresa al Titular, a fin de que este último pueda tomar las medidas correspondientes 
  a la defensa de sus derechos.
  </p>

